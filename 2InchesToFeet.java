
public class InchesToFeet {
	
	final static double CONVERSION_FEET = 0.0833333;
	 
	
	public static void main(String[] args) {
		double inches = 72;
		double inchesToFeet = inches * CONVERSION_FEET;
		 System.out.println(inches + " converts to " + inchesToFeet + " feet.");

			
	}

}
